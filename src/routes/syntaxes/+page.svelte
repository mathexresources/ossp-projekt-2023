        <h2>If statement</h2>
        <p>
            “if” statement je základní řídící konstrukce v programovacích jazycích, která umožňuje provádět různé akce podle toho, zda je určitá podmínka pravdivá (true) nebo nepravdivá (false). Zde je příklad použití if statement v několika různých programovacích jazycích:
        </p>
    </div>
    <div class="right">
        <pre>
            if statement:
                # provede se, pokud je podmínka pravdivá
            else:
                # provede se, pokud je podmínka nepravdivá
        </pre>
    </div>
